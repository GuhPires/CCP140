    private void signup_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signup_buttonActionPerformed
        String usr = username.getText();
        String pass = String.valueOf(password.getPassword());
        
        if (usr.isBlank() || pass.isBlank()) {
            JOptionPane.showMessageDialog(
                    null,
                    "Preencha todos os campos para entrar no sistema", 
                    "Erro!", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        User user = User.login(usr, pass);
        
        if (user == null) {
            JOptionPane.showMessageDialog(
                    null,
                    "Nenhum usu√°rio com essas credenciais foi encontrado", 
                    "Erro!", 
                    JOptionPane.ERROR_MESSAGE);
            return;
        };
        
        username.setText("");
        password.setText("");
        
        if (user.getType().equals("student")) new StudentView(user).setVisible(true);
        if (user.getType().equals("university")) new UniversityView(user).setVisible(true);
        
        this.setVisible(false);
    }//GEN-LAST:event_signup_buttonActionPerformed

