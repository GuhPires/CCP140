    public Food findFood(String name) {
        Food found = null;
        
        for(Food f: this.menu) {
            if (f.getProduct().equals(name)) {
                found = f;
                break;
           }
        }
        
        return found;
    }
    
    public void addFood(String product, float price) {
        this.menu.add(new Food(product, price));
    }
    
    public void removeFood(String product) {
        this.menu.removeIf(f -> f.getProduct().equals(product));
    }
    
    public void updateFood(Food product, float price) {
        product.setPrice(price);
    }
    
    public void getFood(String product) {
        Food f = this.findFood(product);
        
        if(f == null) return;
        
        System.out.println(f.toString());
    }
    
    public String printMenu() {
        String str = "";
        str = str + "========== MENU ==========\n";
        for(Food f : this.menu) {
            str = str + f.toString() + "\n";
        }
        str = str + "==========================\n";
        
        return str;
    }

