    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        String product = add_product_txt.getText();
        float price = Float.parseFloat(add_price_txt.getText());
        
        boolean shouldAdd = JOptionPane.showConfirmDialog(
                null,
                "Tem certeza que deseja adicionar o produto "
                        + product + " com preço R$ " + price + "?",
                "Remover Produto",
                JOptionPane.YES_NO_CANCEL_OPTION) == 0;
        
        if (shouldAdd) this.addFood(product, price);
        
        add_product_txt.setText("");
        add_price_txt.setText("");
        
        System.out.println(this.printMenu());
    }//GEN-LAST:event_add_btnActionPerformed

    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed
        String product = add_product_txt.getText();
        float price = Float.parseFloat(add_price_txt.getText());
        
        Food f = this.findFood(product);
        
        if (f == null) {
            JOptionPane.showMessageDialog(
                null,
                "O produto " + product + " não foi encontrado!",
                "ERRO!",
                JOptionPane.ERROR_MESSAGE);
            
            return;
        }
        
        boolean shouldUpdate = JOptionPane.showConfirmDialog(
                null,
                "Tem certeza que deseja editar o produto " + product +
                        " para seu novo preço R$ " + price + "?",
                "Remover Produto",
                JOptionPane.YES_NO_CANCEL_OPTION) == 0;
        
        if (shouldUpdate) this.updateFood(f, price);
        
        add_product_txt.setText("");
        add_price_txt.setText("");
        
        System.out.println(this.printMenu());
    }//GEN-LAST:event_update_btnActionPerformed

